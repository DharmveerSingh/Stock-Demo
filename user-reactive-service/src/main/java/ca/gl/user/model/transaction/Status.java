package ca.gl.user.model.transaction;

/**
 * The Enum Status.
 */
public enum Status {

	/** The initiated. */
	INITIATED,
	/** The payment awaited. */
	PAYMENT_AWAITED,
	/** The stock awaited. */
	STOCK_AWAITED,
	/** The error. */
	ERROR,
	/** The out of stock. */
	OUT_OF_STOCK,
	/** The aborted. */
	ABORTED,
	/** The stock done. */
	STOCK_DONE,
	/** The failed. */
	FAILED,
	/** The completed. */
	COMPLETED,
	/** The payment failed. */
	PAYMENT_FAILED,
}
